'''
You can use + operator to concatenate 
two or more tuples.
'''
test1 = ("America", "Canada", "Japan", "Italy")
test2 = ("France", "Poland")
print(f'Elements of first tuple: {test1}')
print(f'Elements of second tuple: {test2}')
test3 = test1 + test2
print(f'Elements of third test: {test3}')